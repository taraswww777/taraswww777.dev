/** Количество миллисекунд в одной секунде */
export const MC_1_Second = 1000;

/** Количество миллисекунд в одном минуте */
export const MC_1_Minute = 60 * MC_1_Second;

/** Количество миллисекунд в одном часе */
export const MC_1_Hour = 60 * MC_1_Minute;

/** Количество миллисекунд в одном дне */
export const MC_1_Day = 24 * MC_1_Hour;
